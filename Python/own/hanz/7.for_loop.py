"""
    for loop is used for iterating over a sequence, can be list, tuple, dict, set or string
"""

people = ['John', 'Paul', 'Kelvin', 'Alfred']

for i in people:
    # print(i)
    print(f'Current person:{i}')

# Break

# Continue

# Range
for i in range(10):
    print('hello')

for i in range(len(people)):
    print(f"{people}")
